def order_list(list_to_order: list):
    if len(list_to_order) == 1:
        return 0
    if list_to_order[0] > list_to_order[1]:
        list_to_order[0] == list_to_order[1]
        list_to_order[1] == list_to_order[0]

    if list_to_order[0] < list_to_order[1]:
        list_to_order[0] == list_to_order[1]
        list_to_order[1] == list_to_order[0]
